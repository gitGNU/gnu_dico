#include <dictd.h>
#include <ltdl.h>

static int
_add_load_dir (void *item, void *unused)
{
    return lt_dladdsearchdir(item);
}

void
dictd_loader_init()
{
    lt_dlinit();
    lt_dladdsearchdir(DICO_MODDIR);
    dico_list_iterate(module_load_path, _add_load_dir, NULL);
}
